/**
 * Migration 0.4.9 — point the OpenCode container at AGENTS.md.
 *
 * v0.4.9 introduces OpenCode as the local-mode agent runtime, mirroring the
 * Claude Code / Gemini CLI / Codex / Grok CLI pattern under ai_mode='local'.
 * OpenCode auto-loads AGENTS.md from cwd as its system prompt (same convention
 * as Codex), so the ai_containers row for `opencode` must point there.
 *
 * Pre-0.4.9 seeds.js created the container with instruction_file='CLAUDE.md'
 * (a placeholder from when the row existed but no agent dispatch path used it).
 * After 0.4.9 it must be 'AGENTS.md' so writeAllBackendFiles renders the
 * Personal Assistant constitution there.
 *
 * No template/file regeneration is needed: AgentService.regenerateAllInstructions
 * runs on every server startup (src/server.js:913) and rewrites instruction
 * files for every agent based on the container rows. As soon as OS8 restarts
 * after this migration, every deployed Personal Assistant gets a fresh
 * AGENTS.md generated by claude-md.js::generateAssistantClaudeMd.
 *
 * Idempotent: INSERT OR IGNORE seeds the row in case it's missing entirely
 * (defensive — older DBs missed it altogether), then UPDATE always overwrites
 * the instruction_file. Also hides the row from the terminal dropdown
 * (show_in_terminal=0) — OpenCode is dispatched programmatically, not picked
 * by the user from a dropdown.
 */

module.exports = {
  version: '0.4.9',
  description: 'Point OpenCode container at AGENTS.md so writeAllBackendFiles renders the assistant constitution there',

  async up({ db, logger }) {
    // Defensive: insert the row if missing. Mirrors seeds.js shape.
    const insertResult = db.prepare(
      `INSERT OR IGNORE INTO ai_containers
        (id, provider_id, type, name, command, instruction_file, has_login,
         login_command, api_key_aliases, auth_status_command, auth_file_path,
         login_trigger_args, display_order)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`
    ).run(
      'opencode', 'local', 'cli', 'OpenCode', 'opencode', 'AGENTS.md',
      0, null, '[]', null, null, null, 5
    );

    // Always-overwrite UPDATE so existing rows pick up the rename.
    const updateResult = db.prepare(
      `UPDATE ai_containers
         SET instruction_file = 'AGENTS.md',
             show_in_terminal = 0
       WHERE id = 'opencode'`
    ).run();

    if (insertResult.changes) {
      logger.log(`[0.4.9] Seeded opencode container with instruction_file='AGENTS.md'`);
    } else if (updateResult.changes) {
      logger.log(`[0.4.9] Repointed opencode.instruction_file to AGENTS.md`);
    } else {
      logger.log(`[0.4.9] opencode container already current; no changes`);
    }
  }
};
